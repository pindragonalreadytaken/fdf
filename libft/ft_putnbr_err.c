#include "libft.h"

void	ft_putnbr_err(int n)
{
	if (n == -2147483648)
		ft_puterr("-2147483648");
	else
	{
		if (ft_abs(n) >= 10)
			ft_putnbr_err(n / 10);
		else if (n < 0)
			ft_putchar_err('-');
		ft_putchar_err(ft_abs(n % 10) + '0');
	}
}
