#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	char	*d;

	d = dest;
	i = 0;
	while (*d)
	{
		d++;
	}
	while (src[i])
	{
		d[i] = src[i];
		i++;
	}
	d[i] = '\0';
	return (dest);
}
