#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
	}
	else
	{
		if (ft_abs(n) >= 10)
		{
			ft_putnbr(n / 10);
		}
		else
		{
			if (n < 0)
				ft_putchar('-');
		}
		ft_putchar(ft_abs(n % 10) + '0');
	}
}
